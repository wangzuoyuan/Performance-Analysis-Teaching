#!/usr/bin/env node
// Regenerates the (gitignored) build scaffolding this design-sync needs, from
// the committed config.json. Run from the repo root before building on a fresh
// clone:  node .design-sync/regen-scaffold.mjs
//
// Produces, inside frontend/ (all gitignored):
//   .ds-barrel.tsx        — re-exports every DS component for the IIFE bundle
//   .ds-shim.ts           — process shim (Next.js internals read process.env at
//                           module init; the claude.ai/design runtime has none)
//   .ds-tailwind.config.js— extends the app config to also scan previews
//   .ds-compiled.css      — Tailwind utilities for cssEntry (needs tailwind CLI)
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFileSync } from 'node:child_process';

const DS = dirname(fileURLToPath(import.meta.url));
const ROOT = dirname(DS);
const FE = join(ROOT, 'frontend');
const cfg = JSON.parse(readFileSync(join(DS, 'config.json'), 'utf8'));

// Components whose source uses `export default` (need `{ default as Name }`);
// everything else is re-exported with `export *`.
const DEFAULT_EXPORTS = new Set([
  'AuthGate', 'ChatDrawer', 'BackupCard', 'HomeworkCard', 'StudentNotes',
  'ToolCallCard', 'WeeklyFocusCard', 'BandTrendChart', 'RankBandStackedBar',
  'SubjectScatter', 'TrendLineChart',
]);

const lines = ['// @generated by .design-sync/regen-scaffold.mjs — do not edit', "import './.ds-shim';"];
const starDone = new Set();
for (const [name, src] of Object.entries(cfg.componentSrcMap)) {
  if (src === null) continue;
  const rel = './' + String(src).replace(/\.tsx$/, '');
  if (DEFAULT_EXPORTS.has(name)) lines.push(`export { default as ${name} } from '${rel}';`);
  else if (!starDone.has(rel)) { starDone.add(rel); lines.push(`export * from '${rel}';`); }
}
writeFileSync(join(FE, '.ds-barrel.tsx'), lines.join('\n') + '\n');

writeFileSync(join(FE, '.ds-shim.ts'),
  '// design-sync: browser shim so Next.js internals reading process.env at module\n' +
  '// init don\'t crash the IIFE bundle (claude.ai/design runtime has no `process`).\n' +
  ';(globalThis as any).process = (globalThis as any).process || { env: {} };\n' +
  ';(globalThis as any).process.env = (globalThis as any).process.env || {};\n' +
  'export {};\n');

writeFileSync(join(FE, '.ds-tailwind.config.js'),
  "const base = require('./tailwind.config.js');\n" +
  'module.exports = {\n  ...base,\n  content: [\n' +
  "    './src/**/*.{js,ts,jsx,tsx,mdx}',\n" +
  "    '../.design-sync/previews/**/*.{ts,tsx}',\n  ],\n};\n");

console.log('regenerated .ds-barrel.tsx, .ds-shim.ts, .ds-tailwind.config.js in frontend/');

// Compile Tailwind → cssEntry (best-effort; needs the app's tailwind CLI installed).
const bin = join(FE, 'node_modules', '.bin', 'tailwindcss');
if (existsSync(bin)) {
  execFileSync(bin, ['-c', '.ds-tailwind.config.js', '-i', 'src/app/globals.css', '-o', '.ds-compiled.css', '--minify'],
    { cwd: FE, stdio: 'inherit' });
  console.log('compiled frontend/.ds-compiled.css');
} else {
  console.log('! tailwind CLI not found — run `npm i` in frontend/, then re-run to build .ds-compiled.css');
}
